public class Pessoa {
    private String nome;
    private int idade;
    private String endereco;

    //get e set nome

    public String getNome(){
        return nome;
    }
    void setNome(String nome){
        this.nome = nome;
    }

    // get e set idade

    public int getIdade(){
        return idade;
    }
    void setIdade(int idade){
        this.idade = idade;
    }

    // get e set endereco

    public String getEndereco(){
        return endereco;
    }
    void setEndereco(String endereco){
        this.endereco = endereco;
    }




}
